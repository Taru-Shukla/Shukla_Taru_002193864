    public void populateOrderTable() {
        ArrayList<Restaurant> resDir = business.getRestaurantDirectory().getRestaurantList();
        DefaultTableModel table = (DefaultTableModel) jTable3.getModel();
        table.setRowCount(0);
        for (Restaurant r : resDir) {
            for (Order o : r.getOrderDir()) {
                if (o.getCustomer().getUsername().equals(userAccount.getUsername())) {

                    Object[] row = new Object[4];
                    row[0] = o.getRestaurant().getRestaurantName();
                    row[1] = o.getDeliveryBoy() == null ? "Not Assigned" : o.getDeliveryBoy().getDeliveryManName();
                    row[2] = o.getOrderStatus();
                    row[3] = o.getTotalPrice();
                    table.addRow(row);
                }
            }

        }
    }
