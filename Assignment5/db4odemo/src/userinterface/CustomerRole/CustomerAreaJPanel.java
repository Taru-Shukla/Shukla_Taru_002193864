    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int rowI = jTable2.getSelectedRow();
        String restaurantName = (String) jTable5.getValueAt(jTable5.getSelectedRow(), 0);
        DefaultTableModel table = (DefaultTableModel) jTable4.getModel();
        table.setRowCount(table.getRowCount());
        ArrayList<Menu> menuList = business.getRestaurantDirectory().findRestaurantByRes(restaurantName).getRestaurantMenu();
        for (Menu m : menuList) {
            if (m.getDishName().equals((String) jTable2.getValueAt(rowI, 0))) {
                menuItems.add(m);
                Object[] data = new Object[2];
                data[0] = m.getDishName();
                data[1] = m.getPrice();

                table.addRow(data);
                table.setRowCount(table.getRowCount());
                value += Integer.valueOf(m.getPrice());
            }
        }
        business.getRestaurantDirectory().findRestaurantByRes(resName).getOrder().setTotalPrice(value);
        value = 0;
    }//GEN-LAST:event_jButton1ActionPerformed

